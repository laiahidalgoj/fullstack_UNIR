# Operators

number_a = 12
number_b = 3

print('sum', number_a + number_b)
print('subtract', number_a - number_b)
print('multiply', number_a * number_b)
print('divide', number_a / number_b)
print('entire divide', number_a // number_b)
print('module', number_a % number_b)

# Comparison >, <, >=, <=, !=, ==. return True or False
print(number_a == number_b)
 
# logical operators NOT, AND, OR 

brand = 'apple'
price = 600

print(brand == 'samsung' and price <= 700)
print(brand == 'samsung' or price <= 700)

is_active = True;
print(not is_active)