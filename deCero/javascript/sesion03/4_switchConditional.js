// switch, allows to evaluate some cases and return the result.

let color = 'brown';

switch(color.toLowerCase()){
  case 'red':
    console.log('rojo')
  break;
  case 'blue':
    console.log('azul')
  break;
  case 'yellow':
    console.log('amarillo')
  break;

  default:
    console.log('don\'t have that word translated')

}